//Crie uma variável com uma string e utilize métodos de manipulação de strings, como toUpperCase, toLowerCase, slice ou outros, para modificar a string original. Exiba os resultados finais no console.

const texto = "Texto Aqui";

console.log(texto.toUpperCase() + ' para deixar em letras maiúsculas.');
console.log(texto.toLowerCase() + ' para deixar em letras minúsculas.');
console.log(texto.length + ' para saber a quantidade de caracteres.');
console.log(texto.slice(0, 5) + ' para retornar apenas uma parte da string.');